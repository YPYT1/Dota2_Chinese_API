def translate_enums():
    with open('data/luaapi/enums_cn.json', 'r', encoding='utf-8') as f:
        translated = json.load(f)

    # 翻译所有未翻译的枚举成员 (使用 members 字段)
    member_count = 0
    for trans_enum in translated['items']:
        # 检查 members 字段
        if 'members' in trans_enum:
            for member in trans_enum['members']:
                if not member.get('description_cn') or member['description_cn'].strip() == '':
                    member['description_cn'] = translate_enum_value(trans_enum['name'], member['name'])
                    member_count += 1
        
        # 也检查 values 字段（以防万一）
        if 'values' in trans_enum:
            for value in trans_enum['values']:
                if not value.get('description_cn') or value['description_cn'].strip() == '':
                    value['description_cn'] = translate_enum_value(trans_enum['name'], value['name'])
                    member_count += 1
    
    # 保存文件
    with open('data/luaapi/enums_cn.json', 'w', encoding='utf-8') as f:
        json.dump(translated, f, ensure_ascii=False, indent=2)
    
    print(f"翻译了 {member_count} 个枚举成员")


if __name__ == '__main__':
    translate_enums()
